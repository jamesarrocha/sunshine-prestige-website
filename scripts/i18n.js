const STORAGE_KEY = 'sp-locale';

window.SP_I18N = {
  dict: I18N,
  current: (() => {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved === 'en' || saved === 'es') return saved;
    return (navigator.language || '').toLowerCase().startsWith('es') ? 'es' : 'en';
  })(),
  t(key) { return I18N[this.current][key] ?? key; },
  setLocale(loc) {
    this.current = loc;
    localStorage.setItem(STORAGE_KEY, loc);
    this.apply();
  },
  apply() {
    document.documentElement.lang = this.current;
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      if (I18N[this.current][key] !== undefined) el.innerHTML = I18N[this.current][key];
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.dataset.i18nPlaceholder;
      if (I18N[this.current][key] !== undefined) el.setAttribute('placeholder', I18N[this.current][key]);
    });
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
      const key = el.dataset.i18nAria;
      if (I18N[this.current][key] !== undefined) el.setAttribute('aria-label', I18N[this.current][key]);
    });
    document.querySelectorAll('.lang-toggle button').forEach(b => {
      b.setAttribute('aria-pressed', b.dataset.lang === this.current ? 'true' : 'false');
    });
    if (window.SP_RENDER) window.SP_RENDER.all();
  },
};
